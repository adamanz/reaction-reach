def create_and_verify_draft(email_data, test_name):
    """Create a draft and verify it was created"""
    
    try:
        # Create temporary file
        with tempfile.NamedTemporaryFile(mode='w', suffix='.json', delete=False) as f:
            json.dump(email_data, f, indent=2)
            temp_file = f.name
        
        print(f"ğŸ“ Creating {test_name}...")
        print(f"ğŸ“§ Subject: {email_data['subject']}")
        
        # Call Gmail MCP
        result = subprocess.run([
            "npx", "@gongrzhe/server-gmail-autoauth-mcp",
            "draft_email",
            "--input", temp_file
        ], capture_output=True, text=True, timeout=60)
        
        print(f"ğŸ“¤ Command executed")
        print(f"ğŸ”„ Return code: {result.returncode}")
        
        if result.stdout.strip():
            print(f"ğŸ“¤ Output: {result.stdout}")
        
        if result.stderr.strip():
            print(f"ğŸš¨ Errors: {result.stderr}")
        
        if result.returncode == 0:
            print(f"âœ… {test_name} SUCCESS!")
            return True
        else:
            print(f"âŒ {test_name} FAILED!")
            return False
            
    except Exception as e:
        print(f"âŒ Exception creating {test_name}: {e}")
        return False
    finally:
        if 'temp_file' in locals() and os.path.exists(temp_file):
            os.unlink(temp_file)

